n1=0:1:20;
a=1;
x2=a*sin(0.1*pi*n1);
subplot(3,1,1);
stem(n1,x2);
xlabel('n--->');
ylabel('y[n]--->');
title('original');
x3=a*sin(0.1*pi*(n1*0.5));
subplot(3,1,2);
stem(n1,x3);
xlabel('n--->');
ylabel('y[n]--->');
title('Expansion');
x4=a*sin(0.1*pi*(n1*2));
subplot(3,1,3);
stem(n1,x4);
xlabel('n--->');
ylabel('y[n]--->');
title('Compression');